# Information about functions, primitive types, and loops

# Functions
# - print(): Displays a message on the screen.
# - input(): Allows the user to enter a value from the console.
# - len(): Returns the length (number of elements) of a sequence.
# - range(): Generates a sequence of numbers.
# - int(): Converts a value to an integer.
# - str(): Converts a value to a string.
# - float(): Converts a value to a floating-point number.
# - list(): Converts an iterable object to a list.
# - sorted(): Sorts a list.
# - sum(): Calculates the sum of the elements in a sequence.
# - max(): Returns the largest element in a sequence.
# - min(): Returns the smallest element in a sequence.

# Primitive Types
# - Integer (int): Represents integer numbers.
# - Floating-point number (float): Represents decimal numbers.
# - String (str): Represents a sequence of characters.
# - List (list): Represents an ordered collection of elements.
# - Boolean (bool): Represents a logical value (True or False).

# Loops
# - for Loop: Iterates over a sequence and executes a block of code for each element.
# - while Loop: Repeats a block of code as long as a condition is true.
